import type {
  AtscriptDoc,
  SemanticAnnotateNode,
  SemanticArrayNode,
  SemanticInterfaceNode,
  SemanticNode,
  SemanticPrimitiveNode,
  SemanticPropNode,
  SemanticRefNode,
  SemanticStructureNode,
  SemanticTypeNode,
  TPrimitiveTypeDef,
} from '@atscript/core'
import {
  flattenInterfaceNode,
  hasNavPropAnnotation,
  isArray,
  isConst,
  isGroup,
  isInterface,
  isPrimitive,
  isRef,
  isStructure,
  type TFlatFieldDescriptor,
} from '@atscript/core'

import { type TTsPluginOptions, resolveJsonSchemaMode } from '../plugin'
import { BaseRenderer } from './base-renderer'
import { escapeQuotes, wrapProp } from './utils'

export class TypeRenderer extends BaseRenderer {
  constructor(
    doc: AtscriptDoc,
    private opts?: TTsPluginOptions
  ) {
    super(doc)
  }

  pre() {
    this.writeln('// prettier-ignore-start')
    this.writeln('/* eslint-disable */')
    this.writeln('/* oxlint-disable */')
    this.writeln(`/// <reference path="./${this.doc.name}" />`)
    this.writeln('/**')
    this.writeln(' * 🪄 This file was generated by Atscript')
    this.writeln(' * Do not edit this file!')
    this.writeln(' */')
    this.writeln()
    this.writeln(
      'import type { TAtscriptTypeObject, TAtscriptTypeComplex, TAtscriptTypeFinal, TAtscriptTypeArray, TAtscriptAnnotatedType, TMetadataMap, Validator, TValidatorOptions } from "@atscript/typescript/utils"'
    )
  }

  post() {
    this.writeln('// prettier-ignore-end')
  }

  renderTypeDefString(def?: SemanticNode) {
    const newThis = new TypeRenderer(this.doc, this.opts)
    newThis.renderTypeDef(def)
    return newThis.toString()
  }

  renderTypeDef(def?: SemanticNode) {
    if (!def) {
      this.write('unknown')
      return
    }
    if (isStructure(def)) {
      return this.renderStructure(def)
    }
    if (isGroup(def)) {
      const tuple = def.entity === 'tuple'
      const operator = tuple ? ', ' : ` ${def.op!} `
      const children = def.unwrap()
      const hasSubgroup = !tuple && children.some(c => c.entity === 'group')
      this.write(tuple ? '[' : hasSubgroup ? '(' : '')
      this.renderTypeDef(def.first)
      for (const child of children.slice(1)) {
        this.write(operator)
        this.renderTypeDef(child)
      }
      return this.write(tuple ? ']' : hasSubgroup ? ')' : '')
    }
    if (isConst(def)) {
      const name =
        def.token('identifier')?.type === 'number' ? def.id! : `"${escapeQuotes(def.id!)}"`
      return this.write(name)
    }
    if (isRef(def)) {
      const node = def as SemanticRefNode
      const unwound = this.doc.unwindType(node.id!, node.chain)
      if (isPrimitive(unwound?.def)) {
        this.write(renderPrimitiveTypeDef(unwound.def.config.type))
        if (node.hasChain) {
          this.write(` /* ${node.chain.map(c => c.text).join('.')} */`)
        }
        return
      }
      let name = node.id!
      for (const c of node.chain) {
        name += `["${escapeQuotes(c.text)}"]`
      }
      return this.write(name)
    }
    if (isArray(def)) {
      const node = def as SemanticArrayNode
      const def2 = node.getDefinition()
      const isGrp = def2?.entity === 'group'
      if (isGrp) {
        this.write('(')
      }
      this.renderTypeDef(def2)
      if (isGrp) {
        this.write(')')
      }
      return this.write('[]')
    }
    if (isPrimitive(def as SemanticNode)) {
      return this.write(renderPrimitiveTypeDef((def as unknown as SemanticPrimitiveNode).config.type))
    }
  }

  renderStructure(struct: SemanticStructureNode, asClass?: string, interfaceNode?: SemanticInterfaceNode, filterProps?: Map<string, SemanticPropNode>) {
    this.blockln('{}')
    const patterns = [] as SemanticPropNode[]
    const propsDefs = new Set<string>()

    for (const prop of struct.props.values()) {
      if (filterProps?.has(prop.id!)) {
        continue
      }
      if (prop.token('identifier')?.pattern) {
        if (!filterProps) {
          patterns.push(prop)
        }
        continue
      }
      const phantomType = this.phantomPropType(prop.getDefinition())
      if (phantomType) {
        this.writeln(`// ${prop.id!}: ${phantomType}`)
        continue
      }
      const optional = !!prop.token('optional')
      this.write(wrapProp(prop.id!), optional ? '?' : '', ': ')
      const renderedDef = this.renderTypeDefString(prop.getDefinition())
      propsDefs.add(renderedDef)
      renderedDef.split('\n').forEach(l => this.writeln(l))
    }
    if (patterns.length > 0) {
      this.write(`[key: string]: `)
      for (const prop of patterns) {
        propsDefs.add(this.renderTypeDefString(prop.getDefinition()))
      }
      const defs = Array.from(propsDefs)
      if (defs.length > 1) {
        this.indent()
        for (const def of defs) {
          this.writeln()
          this.write('| ')
          def.split('\n').forEach(l => this.write(l.trim()))
        }
        this.unindent()
        this.writeln()
      } else {
        defs[0].split('\n').forEach(l => this.writeln(l))
      }
    }
    if (asClass) {
      this.renderStaticDeclarations(asClass, interfaceNode)
    }
    this.pop()
  }

  private renderStaticDeclarations(asClass: string, interfaceNode?: SemanticInterfaceNode) {
    this.writeln('static __is_atscript_annotated_type: true')
    this.writeln(`static type: TAtscriptTypeObject<keyof ${asClass}, ${asClass}>`)
    this.writeln(`static metadata: TMetadataMap<AtscriptMetadata>`)
    this.writeln(
      `static validator: (opts?: Partial<TValidatorOptions>) => Validator<typeof ${asClass}>`
    )
    if (resolveJsonSchemaMode(this.opts) === false) {
      this.writeln(
        "/** @deprecated JSON Schema support is disabled. Calling this method will throw a runtime error. To enable, set `jsonSchema: 'lazy'` or `jsonSchema: 'bundle'` in tsPlugin options, or add `@emit.jsonSchema` annotation to individual interfaces. */"
      )
    }
    this.writeln('static toJsonSchema: () => any')
    if (!this.opts?.exampleData) {
      this.writeln(
        '/** @deprecated Example Data support is disabled. To enable, set `exampleData: true` in tsPlugin options. */'
      )
    }
    this.writeln('static toExampleData?: () => any')
    if (interfaceNode && this.hasDbEntity(interfaceNode)) {
      this.renderFlat(interfaceNode)
      this.renderOwnProps(interfaceNode)
      this.renderNavProps(interfaceNode)
      this.renderPk(interfaceNode)
    }
  }

  renderInterface(node: SemanticInterfaceNode) {
    this.writeln()
    const exported = node.token('export')?.text === 'export'
    this.renderJsDoc(node)
    this.write(exported ? 'export declare ' : 'declare ')

    if (node.hasExtends) {
      const firstParent = node.extendsTokens[0].text
      this.write(`class ${node.id!} extends ${firstParent} `)
      // Resolve merged structure from all parents + own props
      const resolved = this.doc.resolveInterfaceExtends(node)
      if (resolved?.entity === 'structure') {
        // Filter out first parent's props (they come via TS extends)
        const firstParentUnwound = this.doc.unwindType(firstParent)
        let firstParentProps: Map<string, SemanticPropNode> | undefined
        if (firstParentUnwound?.def) {
          let fpDef = firstParentUnwound.def
          if (isInterface(fpDef)) {
            // Recursively resolve if first parent also has extends
            if ((fpDef as SemanticInterfaceNode).hasExtends) {
              const fpResolved = firstParentUnwound.doc.resolveInterfaceExtends(
                fpDef as SemanticInterfaceNode
              )
              if (fpResolved && isStructure(fpResolved)) {
                firstParentProps = fpResolved.props
              }
            }
            if (!firstParentProps) {
              fpDef = fpDef.getDefinition() || fpDef
            }
          }
          if (!firstParentProps && isStructure(fpDef)) {
            firstParentProps = fpDef.props
          }
        }
        // Render only props NOT in first parent
        this.renderStructure(
          resolved as SemanticStructureNode,
          node.id!,
          node,
          firstParentProps
        )
      } else {
        this.writeln('{}')
      }
    } else {
      this.write(`class ${node.id!} `)
      const struct = node.getDefinition()
      if (struct?.entity === 'structure') {
        this.renderStructure(struct as SemanticStructureNode, node.id!, node)
      } else {
        this.writeln('{}')
      }
    }
    this.writeln()
  }

  renderType(node: SemanticTypeNode) {
    this.writeln()
    const exported = node.token('export')?.text === 'export'
    this.renderJsDoc(node)
    this.write(exported ? 'export ' : 'declare ')
    this.write(`type ${node.id!} = `)
    this.renderTypeDef(node.getDefinition())
    this.writeln()
    this.renderTypeNamespace(node)
  }

  renderAnnotate(node: SemanticAnnotateNode) {
    // Only render non-mutating annotate (with alias)
    if (node.isMutating) {
      return
    }
    const targetName = node.targetName
    this.writeln()
    const exported = node.token('export')?.text === 'export'
    this.renderJsDoc(node)

    if (this.isTypeTarget(targetName)) {
      // Target is a type — render as type alias + namespace
      this.write(exported ? 'export ' : 'declare ')
      this.write(`type ${node.id!} = ${targetName}`)
      this.writeln()
      const unwound = this.doc.unwindType(targetName)
      this.renderTypeNamespaceFor(node.id!, unwound?.def)
    } else {
      // Target is an interface — render as class extends
      this.write(exported ? 'export declare ' : 'declare ')
      this.writeln(`class ${node.id!} extends ${targetName} {}`)
    }
  }

  renderTypeNamespace(node: SemanticTypeNode) {
    this.renderTypeNamespaceFor(node.id!, node.getDefinition())
  }

  private renderTypeNamespaceFor(name: string, inputDef?: SemanticNode) {
    this.write(`declare namespace ${name} `)
    this.blockln('{}')
    let typeDef = 'TAtscriptTypeDef'
    if (inputDef) {
      let realDef = inputDef
      if (isRef(inputDef)) {
        realDef = this.doc.unwindType(inputDef.id!, inputDef.chain)?.def || realDef
      }
      realDef = this.doc.mergeIntersection(realDef)
      if (isStructure(realDef) || isInterface(realDef)) {
        typeDef = `TAtscriptTypeObject<keyof ${name}, ${name}>`
      } else if (isGroup(realDef)) {
        typeDef = `TAtscriptTypeComplex<${name}>`
      } else if (isArray(realDef)) {
        typeDef = `TAtscriptTypeArray<${name}>`
      } else if (isPrimitive(realDef)) {
        typeDef = `TAtscriptTypeFinal<${name}>`
      }
    }
    this.writeln(`const __is_atscript_annotated_type: true`)
    this.writeln(`const type: ${typeDef}`)
    this.writeln(`const metadata: TMetadataMap<AtscriptMetadata>`)
    this.writeln(
      `const validator: (opts?: Partial<TValidatorOptions>) => Validator<typeof ${name}>`
    )
    if (resolveJsonSchemaMode(this.opts) === false) {
      this.writeln(
        "/** @deprecated JSON Schema support is disabled. Calling this method will throw a runtime error. To enable, set `jsonSchema: 'lazy'` or `jsonSchema: 'bundle'` in tsPlugin options, or add `@emit.jsonSchema` annotation to individual interfaces. */"
      )
    }
    this.writeln('const toJsonSchema: () => any')
    if (!this.opts?.exampleData) {
      this.writeln(
        '/** @deprecated Example Data support is disabled. To enable, set `exampleData: true` in tsPlugin options. */'
      )
    }
    this.writeln('const toExampleData: (() => any) | undefined')
    this.popln()
  }

  /**
   * Checks whether an interface is a DB entity (`@db.table` or `@db.view.for`).
   *
   * Only DB entities get `__flat`, `__pk`, `__ownProps`, and `__navProps` static properties.
   * These exist solely to improve type-safety for filter expressions, `$select`/`$sort`,
   * and `$with` operations in the DB layer.
   */
  private hasDbEntity(node: SemanticInterfaceNode): boolean {
    return !!node.annotations?.some(a => a.name === 'db.table' || a.name === 'db.view.for')
  }

  /**
   * Renders the `static __flat` property — a map of all dot-notation paths
   * to their TypeScript value types.
   *
   * This enables type-safe autocomplete for filter keys and `$select`/`$sort`
   * paths when using `AtscriptDbTable`. The `FlatOf<T>` utility type extracts
   * this map at the type level.
   *
   * Special rendering rules:
   * - **Intermediate paths** (structures, arrays of structures) → `never`
   *   (prevents `$eq` comparisons, but allows `$select` and `$exists`)
   * - **`@db.json` fields** → `string` (stored as serialized JSON in DB,
   *   not individually queryable)
   * - **Leaf fields** → their original TypeScript type
   */
  private renderFlat(node: SemanticInterfaceNode) {
    this.renderFlatMap('__flat', flattenInterfaceNode(this.doc, node))
  }

  /**
   * Renders the `static __ownProps` property — table-owned fields only (no nav props).
   */
  private renderOwnProps(node: SemanticInterfaceNode) {
    this.renderFlatMap('__ownProps', flattenInterfaceNode(this.doc, node, { skipNavProps: true }), { leadingNewline: true, trailingNewline: true })
  }

  /**
   * Shared renderer for flat-map static properties (`__flat`, `__ownProps`).
   *
   * Special rendering rules:
   * - **Intermediate paths** (structures, arrays of structures) → `never`
   * - **`@db.json` fields** → `string` (stored as serialized JSON in DB)
   * - **Leaf fields** → their original TypeScript type
   */
  private renderFlatMap(
    propName: string,
    flatMap: Map<string, TFlatFieldDescriptor>,
    opts?: { leadingNewline?: boolean; trailingNewline?: boolean }
  ) {
    if (flatMap.size === 0) {
      return
    }
    if (opts?.leadingNewline) {
      this.writeln()
    }
    this.write(`static ${propName}: `)
    this.blockln('{}')
    for (const [path, descriptor] of flatMap) {
      this.write(`"${escapeQuotes(path)}"`)
      if (descriptor.optional) {
        this.write('?')
      }
      this.write(': ')
      if (descriptor.intermediate) {
        this.writeln('never')
      } else if (descriptor.dbJson) {
        this.writeln('string')
      } else {
        // Use the original prop definition for rendering (preserves named refs like Address,
        // and gives the renderer ref nodes it knows how to resolve to TS types).
        // Fall back to descriptor.def for synthetic union merges or missing propNode.
        const originalDef = descriptor.propNode?.getDefinition()
        const defToRender = originalDef && !(isGroup(descriptor.def) && descriptor.def !== originalDef)
          ? originalDef
          : descriptor.def
        const renderedDef = this.renderTypeDefString(defToRender)
        renderedDef.split('\n').forEach(l => this.writeln(l))
      }
    }
    if (opts?.trailingNewline) {
      this.popln()
    } else {
      this.pop()
    }
  }

  /**
   * Renders the `static __navProps` property — a map of navigation property names
   * to their declared TypeScript types.
   *
   * This enables type-safe `$with` in queries: `name` is constrained to known
   * navigation property keys, and nested filter/controls are typed to the target entity.
   */
  private renderNavProps(node: SemanticInterfaceNode) {
    let struct: SemanticNode | undefined
    if (node.hasExtends) {
      struct = this.doc.resolveInterfaceExtends(node)
    }
    if (!struct) {
      struct = node.getDefinition()
    }
    if (!struct || !isStructure(struct)) {
      return
    }

    const structNode = struct as SemanticStructureNode
    const navProps: Array<{ name: string; prop: SemanticPropNode }> = []

    for (const [name, prop] of structNode.props) {
      if (prop.token('identifier')?.pattern) {
        continue
      }
      if (hasNavPropAnnotation(prop)) {
        navProps.push({ name, prop })
      }
    }

    if (navProps.length === 0) {
      return
    }

    this.writeln()
    this.write('static __navProps: ')
    this.blockln('{}')
    for (const { name, prop } of navProps) {
      this.write(`"${escapeQuotes(name)}"`)
      if (prop.token('optional')) {
        this.write('?')
      }
      this.write(': ')
      const propDef = prop.getDefinition()
      if (propDef) {
        const renderedDef = this.renderTypeDefString(propDef)
        renderedDef.split('\n').forEach(l => this.writeln(l))
      } else {
        this.writeln('unknown')
      }
    }
    this.popln()
  }

  private renderPk(node: SemanticInterfaceNode) {
    const isMongoCollection = !!node.annotations?.some(a => a.name === 'db.mongo.collection')

    let struct: SemanticNode | undefined
    if (node.hasExtends) {
      struct = this.doc.resolveInterfaceExtends(node)
    }
    if (!struct) {
      struct = node.getDefinition()
    }
    if (!struct || !isStructure(struct)) {
      return
    }

    const structNode = struct as SemanticStructureNode
    const pkProps: Array<{ name: string; prop: SemanticPropNode }> = []
    // Collect unique index annotations grouped by index name to detect compound indexes
    const uniqueByIndex = new Map<string, Array<{ name: string; prop: SemanticPropNode }>>()
    for (const [name, prop] of structNode.props) {
      if (prop.token('identifier')?.pattern) {
        continue
      }
      // For mongo collections, skip _id — it's represented separately via its actual type in the union
      if (isMongoCollection && name === '_id') {
        continue
      }
      if (prop.countAnnotations('meta.id') > 0) {
        pkProps.push({ name, prop })
      }
      if (prop.annotations) {
        for (const ann of prop.annotations) {
          if (ann.name === 'db.index.unique') {
            // Index name is the first arg, or the field name itself for unnamed indexes
            const indexName = ann.args[0]?.text ?? name
            let group = uniqueByIndex.get(indexName)
            if (!group) {
              group = []
              uniqueByIndex.set(indexName, group)
            }
            group.push({ name, prop })
          }
        }
      }
    }
    // Only single-field unique indexes contribute to __pk (compound indexes need multiple fields)
    const uniqueProps: Array<{ name: string; prop: SemanticPropNode }> = []
    const pkNames = new Set(pkProps.map(p => p.name))
    for (const fields of uniqueByIndex.values()) {
      if (fields.length === 1 && !pkNames.has(fields[0].name)) {
        uniqueProps.push(fields[0])
      }
    }

    if (pkProps.length === 0 && uniqueProps.length === 0 && !isMongoCollection) {
      return
    }

    // Resolve the _id field type for mongo collections
    let mongoIdType: string | undefined
    if (isMongoCollection) {
      const idProp = structNode.props.get('_id')
      if (idProp) {
        mongoIdType = this.renderTypeDefString(idProp.getDefinition()).trim()
      }
      mongoIdType ??= 'string'
    }

    // Collect unique prop rendered types (deduplicated)
    const uniqueTypes: string[] = []
    const seenTypes = new Set<string>()
    for (const { prop } of uniqueProps) {
      const rendered = this.renderTypeDefString(prop.getDefinition()).trim()
      if (!seenTypes.has(rendered)) {
        seenTypes.add(rendered)
        uniqueTypes.push(rendered)
      }
    }

    // Flush any pending content on the current line (e.g. closing `}` from renderFlat's pop())
    this.writeln()

    const uniqueSuffix = uniqueTypes.length > 0 ? ` | ${uniqueTypes.join(' | ')}` : ''

    if (pkProps.length === 0 && !isMongoCollection) {
      // No PK, only unique indexes
      this.writeln(`static __pk: ${uniqueTypes.join(' | ')}`)
    } else if (pkProps.length === 0) {
      // Mongo collection with no @meta.id — _id type + unique indexes
      this.writeln(`static __pk: ${mongoIdType}${uniqueSuffix}`)
    } else if (pkProps.length === 1) {
      this.write('static __pk: ')
      if (isMongoCollection) {
        this.write(`${mongoIdType} | `)
      }
      const renderedDef = this.renderTypeDefString(pkProps[0].prop.getDefinition()).trim()
      this.writeln(`${renderedDef}${uniqueSuffix}`)
    } else {
      this.write('static __pk: ')
      if (isMongoCollection) {
        this.write(`${mongoIdType} | `)
      }
      this.blockln('{}')
      for (const { name, prop } of pkProps) {
        this.write(wrapProp(name), ': ')
        const renderedDef = this.renderTypeDefString(prop.getDefinition())
        renderedDef.split('\n').forEach(l => this.writeln(l))
      }
      this.pop()
      this.writeln(uniqueSuffix)
    }
  }

  private phantomPropType(def?: SemanticNode): string | undefined {
    if (!def) {
      return undefined
    }
    if (isPrimitive(def) && def.config.type === 'phantom') {
      return def.id!
    }
    if (isRef(def)) {
      const ref = def as SemanticRefNode
      const unwound = this.doc.unwindType(ref.id!, ref.chain)?.def
      if (isPrimitive(unwound) && unwound.config.type === 'phantom') {
        return ref.hasChain ? `${ref.id!}.${ref.chain.map(c => c.text).join('.')}` : ref.id!
      }
    }
    return undefined
  }

  private isTypeTarget(name: string, doc?: AtscriptDoc): boolean {
    const d = doc || this.doc
    const decl = d.getDeclarationOwnerNode(name)
    if (!decl?.node) {
      return false
    }
    if (decl.node.entity === 'type') {
      return true
    }
    if (decl.node.entity === 'interface') {
      return false
    }
    if (decl.node.entity === 'annotate') {
      return this.isTypeTarget((decl.node as SemanticAnnotateNode).targetName, decl.doc)
    }
    return false
  }

  renderJsDoc(node: SemanticNode) {
    const range = node.token('identifier')?.range
    const rangeStr = range ? `:${range.start.line + 1}:${range.start.character + 1}` : ''
    this.writeln(`/**`)
    this.writeln(` * Atscript ${node.entity} **${node.id!}**`)
    this.writeln(` * @see {@link ./${this.doc.name}${rangeStr}}`)
    this.writeln(` */`)
  }
}

function renderPrimitiveTypeDef(def?: TPrimitiveTypeDef): string {
  if (!def) {
    return 'unknown'
  }
  // If it's a direct final type, return it
  if (typeof def === 'string') {
    return def === 'void' ? 'undefined' : def
  }

  switch (def.kind) {
    case 'final': {
      return def.value === 'void' ? 'undefined' : def.value
    }
    case 'union': {
      return def.items.map(renderPrimitiveTypeDef).join(' | ')
    }
    case 'intersection': {
      return def.items.map(renderPrimitiveTypeDef).join(' & ')
    }
    case 'tuple': {
      return `[${def.items.map(renderPrimitiveTypeDef).join(', ')}]`
    }
    case 'array': {
      return `${renderPrimitiveTypeDef(def.of)}[]`
    }
    case 'object': {
      const props = Object.entries(def.props)
        .map(
          ([key, val]) =>
            `${wrapProp(key)}${
              typeof val === 'object' && val.optional ? '?' : ''
            }: ${renderPrimitiveTypeDef(val)}`
        )
        .join('; ')
      return `{ ${props} }`
    }
    default: {
      // Fallback in case of unexpected input
      return 'unknown'
    }
  }
}
